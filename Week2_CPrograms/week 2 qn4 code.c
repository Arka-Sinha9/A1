#include<stdio.h>
#include<stdlib.h>

int main()
{
    float c,s,t;
    printf("Enter CIE marks: ");
    scanf("%f",&c);
    printf("Enter SEE marks: ");
    scanf("%f",&s);
    s=s/2;
    t=c+s;
    if(t>90 && t<=100)
    {
        printf("The grade is S!!");
    }
    else if(t>80 && t<=90)
    {
        printf("The grade is A!!");
    }
    else if(t>70 && t<=80)
    {
        printf("The grade is B!!");
    }
    else if(t>60 && t<=70)
    {
        printf("The grade is C!!");
    }
    else if(t>50 && t<=60)
    {
        printf("The grade is D!!");
    }
    else if(t>40 && t<=50)
    {
        printf("The grade is E!!");
    }
    else if(t>=0 && t<=40)
    {
        printf("The grade is F!!");
    }
    else
    {
        printf("Marks not valid!!");
    }
    return 0;
}
